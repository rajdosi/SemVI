	MOV SS,AX
	MOV SP,TOS
	PALLINDROME PROC FAR
	
	MOV CL,[SI+1]
	INC SI
	INC SI
	MOV DI,SI
	MOV CH,00H
	ADD DI,CX
	DEC DI
	SHR CL,01H
PASS:	MOV BL,[SI]
	MOV BH,[DI]
	CMP BL,BH
	JNZ NOTPALLIN
	INC SI
	DEC DI
	DEC CL
	JNZ PASS
	MOV ANS,77H
	JMP DONEN
NOTPALLIN: MOV ANS,88H
DONEN:   NOP
	RET
	ENDP

MYPROCEDURE ENDS

